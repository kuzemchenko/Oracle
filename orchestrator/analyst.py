# -*- coding: utf-8 -*-
"""orchestrator/analyst.py — АНАЛИТИЧЕСКИЙ РАЗБОР research-идеи (поток картографа, РЕШЕНИЕ A).

Превращает сухую находку «новость → каскад → компания» в человекочитаемый разбор с ВИДИМОЙ цепочкой
рассуждений (§8 поля 1–13, но простым языком): что в новостях → цепочка эффектов по порядкам →
следствия высоких порядков → рекомендация → обоснование → КРИТИКА. Развязка семей моделей (П10):
конструктив пишет «генератор», критику — «критик» ДРУГОЙ семьи. Это research-разбор (§8.13: инструмент,
не инвестрекомендация); не прошёл полный слепой суд 21-агентного контура — помечаем честно.

П8: без выдуманных чисел; «нет данных» — допустимо. Язык — ПОПУЛЯРНЫЙ: любой термин поясняется.
"""
import json

ANALYST_SYSTEM = (
    "Ты — аналитик-объяснитель «Оракула». Тебе дают НАХОДКУ: новостное событие и черновую каскадную "
    "цепочку эффектов (1–4 порядка) с тикерами. Твоя задача — написать ПОНЯТНЫЙ РАЗБОР для умного "
    "человека БЕЗ финансового образования. Показать ЦЕПОЧКУ РАССУЖДЕНИЙ шаг за шагом.\n\n"
    "ЖЕЛЕЗНЫЕ ПРАВИЛА:\n"
    "• ПОПУЛЯРНО. Никакого жаргона без пояснения. Встретил термин (хедж, бета, чокпоинт, спред) — "
    "тут же объясни простыми словами в скобках. Пиши так, будто объясняешь умному другу за кофе.\n"
    "• ЧЕСТНО (П8). Никаких выдуманных цифр. Нет данных — так и скажи «точно не известно».\n"
    "• Это ИССЛЕДОВАТЕЛЬСКИЙ разбор, НЕ команда покупать/продавать (§8.13). Формулируй как «на что "
    "смотреть» и «что это может значить», а не «купи».\n"
    "• Первый порядок (само событие) обычно уже в цене — рынок отыграл за секунды. Интересен ДАЛЬНИЙ "
    "узел, до которого рынок ещё не дошёл. Объясни, ПОЧЕМУ именно он недооценён.\n\n"
    "Верни РОВНО один объект JSON (значения — строки на русском, простым языком):\n"
    '{"заголовок":"одна фраза-крючок, о чём идея",'
    '"что_в_новостях":"что произошло и почему это вообще важно — 2-3 предложения",'
    '"цепочка_эффектов":[{"порядок":1,"звено":"что","что_происходит":"простое объяснение","тикер":"XXX.US|null"}],'
    '"следствия_высоких_порядков":"что меняется на 2-4 порядке: кто выигрывает, кто теряет и почему — простыми словами",'
    '"идея":{"тикер":"XXX.US","компания":"как называется","что_за_компания":"чем занимается простыми словами",'
    '"направление":"скорее вверх|скорее вниз|неясно","почему_она":"почему именно эта компания — недооценённое дальнее звено"},'
    '"рекомендация":"на что смотреть наблюдателю; это НЕ совет купить, а гипотеза для наблюдения",'
    '"обоснование":"почему цепочка должна сработать — механизм простыми словами",'
    '"что_неизвестно":["честный список того, чего мы не знаем"]}'
)

CRITIC_SYSTEM = (
    "Ты — въедливый скептик «Оракула», ДРУГАЯ модель-семья, чем аналитик (П10 — состязательность). "
    "Тебе дают разбор research-идеи. Твоя задача — ЧЕСТНО РАЗБИТЬ его, найти слабые места. Не "
    "поддакивай. Если идея слабая — так и скажи.\n\n"
    "ПРАВИЛА: популярно (объясняй термины), честно (П8, без выдумок). Думай как тот, кто стоит на "
    "ДРУГОЙ стороне сделки и считает, что мы неправы.\n\n"
    "Верни РОВНО один объект JSON (на русском, простым языком):\n"
    '{"кто_на_другой_стороне":"кто продаёт нам/ставит против и почему он может быть прав",'
    '"что_обрушит_тезис":["конкретные события/условия, при которых идея рассыпается"],'
    '"самое_слабое_звено":"какое звено цепочки самое непроверенное и почему",'
    '"вердикт":"честный итог: стоит ли вообще смотреть на это, простыми словами",'
    '"уверенность_в_идее":"низкая|средняя|высокая"}'
)


def _parse_json(text):
    txt = (text or "").strip()
    start, end = txt.find("{"), txt.rfind("}")
    if start < 0 or end <= start:
        return None
    try:
        return json.loads(txt[start:end + 1])
    except (json.JSONDecodeError, ValueError):
        return None


def _idea_context(idea):
    """Контекст для модели из записи картограф-идеи (событие + цепочка узлов + цель)."""
    nodes = idea.get("узлы_каскада") or []
    chain_txt = "\n".join(
        f"  порядок {n.get('порядок')}: {n.get('узел')} — тикеры {n.get('тикеры')}"
        f"{' [чокпоинт]' if n.get('чокпоинт') else ''}"
        for n in nodes)
    return (f"СОБЫТИЕ: {idea.get('событие')}\n"
            f"Ключевые слова кластера: {idea.get('ключи')}\n"
            f"Целевая компания-мишень (дальний узел): {idea.get('актив')} "
            f"(узел {idea.get('порядок_узла')}, {'чокпоинт' if idea.get('чокпоинт') else 'обычный узел'})\n"
            f"Черновая цепочка эффектов:\n{chain_txt or '  (нет узлов)'}\n"
            f"Тектонический потенциал (0..1, грубая оценка масштаба каскада): "
            f"{idea.get('тектонический_потенциал')}")


def analyze_idea(idea, client):
    """Полный аналитический разбор одной research-идеи: конструктив (генератор) + критика (критик,
    другая семья П10). Возвращает {разбор, критика} или {} при сбое модели (П8 — не выдумываем)."""
    ctx = _idea_context(idea)
    a = client.complete("generator", ANALYST_SYSTEM,
                        ctx + "\n\nНапиши разбор по контракту из системного промпта.",
                        agent_id="research_analyst", output_kind="generator_hypothesis")
    разбор = _parse_json(a.get("text"))
    if not разбор:
        return {}
    crit_user = (ctx + "\n\nРАЗБОР АНАЛИТИКА (на критику):\n"
                 + json.dumps(разбор, ensure_ascii=False)
                 + "\n\nРазбей его по контракту из системного промпта.")
    c = client.complete("critic", CRITIC_SYSTEM, crit_user,
                        agent_id="research_critic", output_kind="critique")
    критика = _parse_json(c.get("text")) or {"вердикт": "критик не дал валидного ответа (П8)"}
    return {"разбор": разбор, "критика": критика}
