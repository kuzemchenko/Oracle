"""orchestrator/product_rank.py — П2б (§R4.4, подпись владельца 05.07.2026): ПАРАЛЛЕЛЬНЫЙ
продуктовый ранг research-выдачи «неочевидность × свежесть × доказуемость».

ГРАНИЦА СЛОЯ Б (жёсткая): ранжируется только ПОКАЗ продукта — порядок идей в сессии партнёра
(§17.6) и research-дайджесте. Боевой отбор НЕ тронут: скоринг §7, веса §30.6, воронка отбора
графа, треки seal, слепой суд и §11 работают как раньше (автотесты держат границу).

Компоненты (каждая ∈ [0,1], детерминированный код — Инв#6):
  • неочевидность — глубина узла в каскаде: (порядок−1)/3 с кэпом [0,1] (2-й порядок 0.33,
    4-й+ 1.0 — «от общего к частному», дальние узлы обыватель не видит); чокпоинт +0.15 (узкое
    горло — неочевидная точка переноса). Для идей картографа без порядка — по числу звеньев цепи.
  • свежесть — из поля «внимание» П2а (свежесть = 1−score перегретости Trends). НЕ измерена
    (None) → НЕЙТРАЛЬНЫЙ ПРАЙОР 0.5 с честной пометкой в «не_измерено» (правило П2а §R0#5:
    None ≠ 1.0 — отсутствие данных не награда; и не приговор — потому нейтраль, и это видно).
  • доказуемость — надёжность связи r² ∈ [0,1] (для research-гипотез картографа связь ещё не
    мерена → прайор 0.5 с пометкой).

балл = произведение компонент (по ревизии §R4.4 — «×», а не сумма: провал любой компоненты
топит идею в показе, сильные стороны не маскируют слабую).

Поздние фазы датчика (ПОЗДНО/ЛОВУШКА/ОТЫГРАНО) — обязательная ПОМЕТКА в ранге (§R5: такие идеи
«не в топе без явной пометки»). Штраф сверх свежести не выдумываем: у перегретых свежесть и так
низкая, а пометка делает фазу видимой владельцу.
"""

# фазы датчика П1, требующие явной пометки в выдаче (§R5; ОТЫГРАНО — вне канона §8, см. П2а)
LATE_PHASES = ("ПОЗДНО", "ЛОВУШКА", "ОТЫГРАНО")
NEUTRAL_PRIOR = 0.5      # прайор неизмеренной компоненты: нейтрален и ЗАДЕКЛАРИРОВАН в пометке
CHOKEPOINT_BONUS = 0.15  # узкое горло каскада — неочевидная точка переноса


def _clip01(x):
    return max(0.0, min(1.0, float(x)))


def _order_and_chokepoint(idea):
    """(порядок_узла, чокпоинт) из идеи сессии/брифа/картографа — где что лежит."""
    arg = idea.get("аргумент") if isinstance(idea.get("аргумент"), dict) else {}
    order = arg.get("порядок_узла") or idea.get("порядок") or idea.get("порядок_узла")
    chok = arg.get("чокпоинт") if arg.get("чокпоинт") is not None else idea.get("чокпоинт")
    if order is None:
        узлы = arg.get("узлы_каскада") or idea.get("узлы_каскада") or []
        if isinstance(узлы, list) and узлы:
            order = len(узлы)              # глубина цепи картографа как прокси порядка
    return order, bool(chok)


def _reliability(idea):
    arg = idea.get("аргумент") if isinstance(idea.get("аргумент"), dict) else {}
    r2 = arg.get("надёжность_r2")
    if r2 is None:
        r2 = idea.get("надёжность_r2")
    return r2


def rank_idea(idea):
    """Продуктовый ранг одной идеи (dict сессии партнёра ИЛИ брифа протокола ИЛИ картографа).
    Возвращает {"балл", "компоненты", "не_измерено", "поздняя_фаза", "spec_ref"} — только
    посчитанное и прайоры с декларацией (П8)."""
    не_измерено = []

    order, chok = _order_and_chokepoint(idea)
    if order is None:
        неочев = NEUTRAL_PRIOR
        не_измерено.append("неочевидность (нет порядка узла) → прайор 0.5")
    else:
        неочев = _clip01((float(order) - 1.0) / 3.0)
        if chok:
            неочев = _clip01(неочев + CHOKEPOINT_BONUS)

    внимание = idea.get("внимание") if isinstance(idea.get("внимание"), dict) else {}
    свежесть = внимание.get("свежесть")
    if свежесть is None:
        свежесть = NEUTRAL_PRIOR
        не_измерено.append("свежесть (внимание не измерено) → прайор 0.5")
    else:
        свежесть = _clip01(свежесть)

    r2 = _reliability(idea)
    if r2 is None:
        доказ = NEUTRAL_PRIOR
        не_измерено.append("доказуемость (r² связи не мерен) → прайор 0.5")
    else:
        доказ = _clip01(r2)

    фаза = внимание.get("фаза")
    поздняя = фаза if фаза in LATE_PHASES else None

    return {
        "балл": round(неочев * свежесть * доказ, 4),
        "компоненты": {"неочевидность": round(неочев, 4), "свежесть": round(свежесть, 4),
                       "доказуемость": round(доказ, 4)},
        "не_измерено": не_измерено,
        "поздняя_фаза": поздняя,       # §R5: ПОЗДНО/ЛОВУШКА/ОТЫГРАНО не в топе без пометки
        "spec_ref": "П2б §R4.4 (подпись 05.07): показ, не отбор — §7/§30.6 не тронуты",
    }


def annotate_and_sort(ideas):
    """Прикрепить продуктовый_ранг каждой идее (мутирует dict'ы) и вернуть НОВЫЙ список,
    отсортированный по баллу (убыв.). Сортировка стабильна: при равном балле сохраняется
    существующий порядок выдачи (money → провизорный → картограф)."""
    for idea in ideas or []:
        if isinstance(idea, dict):
            idea["продуктовый_ранг"] = rank_idea(idea)
    return sorted(list(ideas or []),
                  key=lambda i: (i.get("продуктовый_ранг") or {}).get("балл") or 0.0,
                  reverse=True)
