def build_one(agent_id):
    m = meta(agent_id)
    parts = [header(m), P8, BODIES[agent_id], CONTRACTS[m["output_kind"]]]
    return "\n\n".join(parts) + "\n"


def main():
    check = "--check" in sys.argv
    PROMPTS.mkdir(parents=True, exist_ok=True)
    drift = []
    for aid, *_ in ALL_AGENTS:
        text = build_one(aid)
        path = PROMPTS / f"{aid}.md"
        if check:
            cur = path.read_text(encoding="utf-8") if path.exists() else None
            if cur != text:
                drift.append(aid)
        else:
            path.write_text(text, encoding="utf-8")
    # самопроверка инварианта: П8 в каждом файле
    missing_p8 = [aid for aid, *_ in ALL_AGENTS
                  if "НУЛЕВЫЕ ВЫДУМКИ" not in build_one(aid)]
    if missing_p8:
        print("ОШИБКА: П8 отсутствует в:", missing_p8); sys.exit(2)
    if check:
        if drift:
            print("РАССИНХРОН (запусти без --check):", drift); sys.exit(1)
        print(f"OK: {len(ALL_AGENTS)} промптов синхронны, П8 в каждом")
    else:
        print(f"Собрано {len(ALL_AGENTS)} промптов в {PROMPTS}/ — П8 в каждом, контракт вывода в каждом")


if __name__ == "__main__":
    main()
